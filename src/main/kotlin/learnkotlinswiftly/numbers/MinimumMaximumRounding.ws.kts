package learnkotlinswiftly.numbers

import kotlin.math.ceil
import kotlin.math.floor

val max = maxOf(70, 100) //To find maximum of 2 numbers
println(max)

val min = minOf(-90, 78) //To find minimum of 2 numbers
println(min)

val floor = floor(4.24) //To round down (must be Float/Double)
println(floor)

val ceiling = ceil(4.24) //To round up (must be Float/Double)
println(ceiling)