package learnkotlinswiftly.numbers

var number = 50

number++ //To increment by 1
println(number)

number-- //To decrement by 1
println(number)

number += 5 //To increment by any amount
println(number)

number -= 5 //To decrement by any amount
println(number)

number *= 5 //To multiply number by any amount (and set it to that product)
println(number)

number /= 10 //To divide number by any amount (and set it to that quotient)
println(number)