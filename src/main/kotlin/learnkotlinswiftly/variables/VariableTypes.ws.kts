package learnkotlinswiftly.variables

val myString = "This is a string value" //holds text
println("String: $myString") //To insert variable in string, prefix variable name with "$"

val myCharacter = 'A' //holds 1 single character
println("Character: $myCharacter")

val myInt = 1000 //holds smaller whole numbers
println("Int: $myInt")

val myLong = 7000000000000 //holds larger whole numbers
println("Long: $myLong")

val myFloat = 1.4F //holds smaller decimals
println("Float: $myFloat")

val myDouble = 1.4 //holds larger decimals
println("Double: $myDouble")

val myBooleanTrue = true //holds true/false
println("Boolean: $myBooleanTrue")

val myBooleanFalse = false //holds true/false
println("Boolean: $myBooleanFalse")
